export function getShenshaCommentary(
  name: string,
  matches: string[] = [],
  basis: string[] = [],
): ShenshaCommentary {
  const seed = COMMENTARY[name as SupportedShenshaName] ?? [
    "这是传统四柱体系中的辅助观察项，需要结合整体命局理解。",
    "现实中只把它当作自我检查与风险管理的一个提醒。",
    "不能脱离整体格局单独判断吉凶。",
  ];
  const matchText = matches.length > 0 ? matches.join("、") : "当前柱位";
  const basisText = basis.length > 0 ? basis.join("；") : "典籍规则";

  return {
    title: `${name}怎么讲`,
    classicMeaning: seed[0],
    practicalReading: seed[1],
    caution: seed[2],
    hitReading: `这张盘的${name}落在${matchText}，查法是：${basisText}。`,
  };
}
